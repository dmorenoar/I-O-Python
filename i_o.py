# 11. EXEMPLE PRÀCTIC: CONVERTIDOR DE TEMPERATURES
# -------------------------------------------------
print("=== 11. Convertidor Celsius a Fahrenheit ===")
celsius = float(input("Temperatura en Celsius: "))
fahrenheit = (celsius * 9/5) + 32
print(f"{celsius}°C equivalen a {fahrenheit}°F")

print("\n" + "="*40 + "\n")

# 12. EXEMPLE PRÀCTIC: CÀLCUL D'ÀREA D'UN RECTANGLE
# --------------------------------------------------
print("=== 12. Càlcul d'àrea d'un rectangle ===")
base = float(input("Base del rectangle (cm): "))
altura = float(input("Altura del rectangle (cm): "))
area = base * altura
perimetre = 2 * (base + altura)

print(f"\nÀrea: {area} cm²")
print(f"Perímetre: {perimetre} cm")

print("\n" + "="*40 + "\n")

