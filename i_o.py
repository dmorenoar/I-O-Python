# 14. EXEMPLE AMB MÚLTIPLES F-STRINGS
# ------------------------------------
print("=== 14. F-strings amb operacions ===")
preu = float(input("Preu del producte (€): "))
quantitat = int(input("Quantitat: "))
descompte = float(input("Descompte (%): "))

subtotal = preu * quantitat
import_descompte = subtotal * (descompte / 100)
total = subtotal - import_descompte

print(f"\nSubtotal: {subtotal:.2f}€")
print(f"Descompte ({descompte}%): -{import_descompte:.2f}€")
print(f"Total a pagar: {total:.2f}€")

print("\n" + "="*40 + "\n")

